public class AutoInc {
    public static void main(String[] args) {
        int i=1;
        System.out.println("BinaryFile : "+i);
        System.out.println("++BinaryFile : "+ ++i);
        System.out.println("BinaryFile++ : "+i++);
        System.out.println("BinaryFile : "+i);
        System.out.println("--BinaryFile : "+ --i);
        System.out.println("BinaryFile-- : "+i--);
        System.out.println("BinaryFile : "+i);
    }
}
